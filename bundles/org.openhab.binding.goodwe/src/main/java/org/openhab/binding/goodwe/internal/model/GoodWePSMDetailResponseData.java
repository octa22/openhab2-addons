package org.openhab.binding.goodwe.internal.model;

import org.eclipse.jdt.annotation.NonNullByDefault;

import com.google.gson.annotations.SerializedName;

/**
 * The {@link GoodWePSMDetailResponseData} class holds detail of the successful PSM
 * detail response.
 *
 * @author Ondrej Pecta - Initial contribution
 */
@NonNullByDefault
public class GoodWePSMDetailResponseData {
    GoodWePowerstationInfo info = new GoodWePowerstationInfo();
    GoodWePowerstationKpi kpi = new GoodWePowerstationKpi();

    @SerializedName(value = "energeStatisticsCharts")
    GoodWePowerstationStats stats = new GoodWePowerstationStats();

    public GoodWePowerstationInfo getInfo() {
        return info;
    }

    public GoodWePowerstationKpi getKpi() {
        return kpi;
    }

    public GoodWePowerstationStats getStats() {
        return stats;
    }
}
